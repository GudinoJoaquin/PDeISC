const suma = (a, b) => {
  return a + b;
};
const resta = (a, b) => {
  return a - b;
};
const multiplicacion = (a, b) => {
  return a * b
}
const division = (a, b) => {
  return a / b
}

console.log(`Suma: ${suma(4, 5)}, Resta: ${resta(3, 6)}, Multiplicacion: ${multiplicacion(2, 7)}, Division: ${division(20, 4)}`)
